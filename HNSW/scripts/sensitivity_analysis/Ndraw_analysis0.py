# 讀取數據
df = pd.read_csv(io.StringIO(data))

# 設定畫布大小 (3 行 x 3 列)
fig, axes = plt.subplots(3, 3, figsize=(20, 16), constrained_layout=True)

# 參數清單與對應的控制變數名稱
params = ["M", "ef", "ef_construction"]

# 右軸指標的設定資訊表
metrics_info = [
    {"col": "qps", "label": "QPS (queries/sec)", "color": "tab:red", "title": "QPS"},
    {"col": "build_time_sec", "label": "Build Time (sec)", "color": "tab:green", "title": "Build Time"},
    {"col": "query_time_sec", "label": "Search Time (sec)", "color": "tab:orange", "title": "Search Time"}
]

# 開始繪製 3x3 矩陣圖
for i, param in enumerate(params):
    # 依據控制變數篩選數據並排序
    sub_df = df[df["varied_param"] == param].sort_values(by=param)
    x = sub_df[param]
    
    for j, metric in enumerate(metrics_info):
        ax1 = axes[i, j]
        
        # --- 左軸：繪製 Recall (不變的基準藍線) ---
        line1, = ax1.plot(x, sub_df["recall_at_10"], color="tab:blue", marker="o", linewidth=2, label="Recall @ 10")
        ax1.set_xlabel(f"Parameter: {param}", fontsize=12, fontweight='bold')
        ax1.set_ylabel("Recall @ 10", color="tab:blue", fontsize=12)
        ax1.tick_params(axis="y", labelcolor="tab:blue", labelsize=10)
        ax1.grid(True, linestyle="--", alpha=0.5) # 僅開啟左軸格線，避免混亂
        
        # --- 右軸：繪製對應的效能指標 (QPS / Build Time / Search Time) ---
        ax2 = ax1.twinx()
        line2, = ax2.plot(x, sub_df[metric["col"]], color=metric["color"], marker="s", linestyle="--", linewidth=1.8, label=metric["title"])
        ax2.set_ylabel(metric["label"], color=metric["color"], fontsize=12)
        ax2.tick_params(axis="y", labelcolor=metric["color"], labelsize=10)
        
        # 動態標題
        ax1.set_title(f"Impact of {param} on Recall & {metric['title']}", fontsize=13, fontweight='bold', pad=10)

# 替整張大圖加上主標題
plt.suptitle("HNSW Multi-Param Sensitivity & Trade-off Analysis", fontsize=18, fontweight='bold', y=1.02)

